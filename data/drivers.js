function getDriver(id) {
  return drivers.find((driver) => driver.id === id) || drivers[0];
}

function getLevel(growth) {
  let current = levels[0];
  levels.forEach((level) => {
    if (growth >= level.minGrowth) current = level;
  });
  return current;
}

